#include<iostream>
using namespace std;
int main() {

    // binary
    cout << 0b01011001 << endl;
    cout << char( 0b01011001 ) << endl;

    //octal
    cout << 0131 << endl;
    cout << char( 0131 ) << endl;

    //decimal
    cout << 89 << endl;
    cout << char( 89 ) << endl;

    //hexadecimal
    cout << 0x59 << endl;
    cout << char( 0x59 ) << endl;
}